type Style = {
  name: string;
  category: 'Bold' | 'Italic' | 'Script' | 'Glitch' | 'Boxed' | 'Circled' | 'Aesthetic' | 'Other';
  transform: (text: string) => string;
}

function applyCharMap(text: string, map: Record<string, string>): string {
  return text.split('').map(char => map[char.toLowerCase() as keyof typeof map] || char).join('');
}

