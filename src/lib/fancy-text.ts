const symbolMap = {
    'a': ['@', 'Ä', 'Å', 'À'], 'b': ['ß'], 'c': ['©', '¢'], 'e': ['€'], 'f': ['ƒ'], 
    'l': ['£'], 'n': ['ñ'], 'o': ['Ø', 'Ö', 'Ò', 'Ó'], 'r': ['®'], 's': ['$'],
    'u': ['Ü', 'Ù', 'Ú'], 'y': ['¥'], 'x': ['×'],
};

fancyTextStyles.push({
  name: "L33t Sp34k",
  category: "Aesthetic",
  transform: (text) => {
    const l33tMap: {[key: string]: string} = {
      'a': '4', 'e': '3', 'g': '6', 'i': '1', 'o': '0', 's': '5', 't': '7'
    };
    return text.toLowerCase().split('').map(char => l33tMap[char] || char).join('');
  }
});

fancyTextStyles.push({
    name: 'Symbolic',
    category: 'Aesthetic',
    transform: (text) => text.toLowerCase().split('').map(char => {
        if (symbolMap[char as keyof typeof symbolMap]) {
            const symbols = symbolMap[char as keyof typeof symbolMap];
            return symbols[Math.floor(Math.random() * symbols.length)];
        }
        return char;
    }).join('')
});

const lines = {
  ' ऊपर': '\u0305',
  ' नीचे': '\u0332',
  ' मध्य': '\u0336'
};

Object.keys(lines).forEach(line => {
  fancyTextStyles.push({
    name: `Line ${line}`,
    category: 'Aesthetic',
    transform: (text) => text.split('').join(lines[line as keyof typeof lines]) + lines[line as keyof typeof lines]
  });
});

